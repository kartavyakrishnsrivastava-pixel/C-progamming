#include <stdio.h>
int main() {
    
    char grade = 'A';
    printf("My grade is %c\n", grade); // printing the value of 'grade' using the %c format specifier for characters//
    
    char initial = 'J';
    printf("My initial is %c\n", initial); // printing the value of 'initial' using the %c format specifier for characters//
    
    char symbol = '$';
    printf("The symbol is %c\n", symbol); // printing the value of 'symbol' using the %c format specifier for characters//
    
    return 0;
}